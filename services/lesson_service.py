THEORY = {
    "переменная": "Переменная — это имя для хранения данных.",
    "цикл for": "for используется для перебора элементов.",
    "функция": "Функция — это блок кода, который можно вызывать.",
    "список": "Список — это изменяемая коллекция.",
    "if": "if используется для условий.",
    "словарь": "Словарь хранит пары ключ-значение."
}


def get_theory_response(user_text: str) -> str:
    text = user_text.lower()

    for key, value in THEORY.items():
        if key in text:
            return value

    return (
        "Я могу объяснить темы по Python.\n"
        "Скажи: объясни переменную, цикл for, функцию, список, if или словарь."
    )