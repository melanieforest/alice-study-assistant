THEORY = {
    "переменная": "Переменная в Python — это имя, которое ссылается на значение в памяти программы.",
    "цикл for": "Цикл for используется для перебора элементов последовательности, например списка, строки или диапазона.",
    "функция": "Функция — это именованный блок кода, который можно вызывать повторно.",
    "список": "Список в Python — это изменяемая коллекция элементов, которая хранится в квадратных скобках.",
}


def get_theory_response(user_text: str) -> str:
    text = user_text.lower()

    for topic, explanation in THEORY.items():
        if topic in text:
            return explanation

    return (
        "Я могу объяснить темы по Python. "
        "Например, скажи: объясни переменную, объясни цикл for или объясни функцию."
    )
